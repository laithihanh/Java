public class Honda_Bai3 {
    private String name;
    private String ma;
    private int thongso;

    public Honda_Bai3(String name, String ma, int thongso) {
        this.name = name;
        this.ma = ma;
        this.thongso = thongso;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public int getThongso() {
        return thongso;
    }

    public void setThongso(int thongso) {
        this.thongso = thongso;
    }

    
    
}
